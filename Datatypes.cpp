#include<iostream>
using namespace std;

int main(){

    int i;
    cout << "size of int "<<sizeof(i)<<endl;

    float f;
    cout << "size of float "<<sizeof(f)<<endl;

    char c;
    cout <<"size of char "<<sizeof(c)<<endl;

    bool b;
    cout <<"size of boolean "<<sizeof(b)<<endl;

    short int si;
    cout <<"size of shortint "<<sizeof(si)<<endl;

    long int li;
    cout <<"size of longint "<<sizeof(li)<<endl;

    return 0;
}